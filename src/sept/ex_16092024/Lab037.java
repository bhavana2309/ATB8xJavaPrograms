package sept.ex_16092024;

public class Lab037 {
    public static void main(String[] args) {
        // Relational operators = boolean
        // > < , >= , <= , != , ! -> true or false
        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_girl = 34;
        int age_boy = 34;
        boolean result = age_boy >= age_girl;
        System.out.println(result);
    }
}

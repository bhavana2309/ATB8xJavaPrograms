package sept.ex_16092024;

public class Lab031 {
    public static void main(String[] args) {
        // Arithmetic operators
        // +, -, *, /
        int a = 10;
        int b = 2;
        System.out.println(a+b); // + -> binary - 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % - Modulus - Remainders
        System.out.println(a%b);

        // 2 | 10 | 5 - quotient
        //   | 10 |
        // -------------
        //      0 - remainder

        System.out.println(67%13);





    }
}
